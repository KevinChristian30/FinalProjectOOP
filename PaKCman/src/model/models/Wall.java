package model.models;

public class Wall {

	public double positionX, positionY, width, height;

	public Wall(double positionX, double positionY, double width, double height) {
		super();
		this.positionX = positionX;
		this.positionY = positionY;
		this.width = width;
		this.height = height;
	}
	
	
}
